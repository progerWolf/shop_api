    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $shops = Shop::with('product_category:id,name')->where('status', Shop::STATUS_PUBLISHE);

        if($request->category_slug) {
            $shops->whereHas('product_category', function($q) use ($request) {
                $q->where('slug', $request->category_slug);
            });
        }

        if($request->q) {
            $shops->where('name', 'like', "%$request->q%")->whereOr('description', 'like', "%$request->q%");
        }

        return ShopResource::collection($shops->paginate(12));
    }

